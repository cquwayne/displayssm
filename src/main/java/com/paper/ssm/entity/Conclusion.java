package com.paper.ssm.entity;

public class Conclusion {

    //    熔化效率
    private Float meltingEfficiency;
    //    焦炭反应性
    private Float cokeReactivity;

    public Float getMeltingEfficiency() {
        return meltingEfficiency;
    }

    public void setMeltingEfficiency(Float meltingEfficiency) {
        this.meltingEfficiency = meltingEfficiency;
    }

    public Float getCokeReactivity() {
        return cokeReactivity;
    }

    public void setCokeReactivity(Float cokeReactivity) {
        this.cokeReactivity = cokeReactivity;
    }
}
